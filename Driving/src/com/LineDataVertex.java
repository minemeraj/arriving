package com;

public class LineDataVertex {
	
	int vertex_index=-1;
	int vertex_texture_index=-1;
	int vertex_texture_x=0;
	int vertex_texture_y=0;
	
	public int getVertex_index() {
		return vertex_index;
	}
	public void setVertex_index(int vertex_index) {
		this.vertex_index = vertex_index;
	}
	public int getVertex_texture_index() {
		return vertex_texture_index;
	}
	public void setVertex_texture_index(int vertex_texture_index) {
		this.vertex_texture_index = vertex_texture_index;
	}
	public double getVertex_texture_x() {
		return vertex_texture_x;
	}

	public double getVertex_texture_y() {
		return vertex_texture_y;
	}

	
}
